def get_embedding(text: str) -> list:
    """OpenAI 임베딩 생성"""
    client = openai.OpenAI(api_key=settings.openai.api_key)
    response = client.embeddings.create(
        model=settings.openai.embedding_model,
        input=text
    )
    return response.data[0].embedding


